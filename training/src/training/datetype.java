package training;

public class datetype {

	public static void main(String[] args) {
	
		//Date type is boolean   storage is 1bit ( true/false)
		
		boolean result=true;
		
		System.out.println("The value is " +result);
		
		
// Date type is char      storage is 2 byte (0 to 65k )
		
		char a='A';
		
		System.out.println("The outcome is " +a);
		
       char b=126; // asii code value 126=~
		
		System.out.println("The outcome is " +b);
		
//Date type is byte  storage is 1byte ( -127 to 126)
		
		byte c=125;
		 
		System.out.println("The byte outcome is " +c);
		
		
 //Date type is short  storage is 2byte ( -32k to 33k)
		
		short d=25000;
		
		System.out.println("The short outcome is " +d);
		
 //Date type is int  storage is 4 byte 
		
			int  e=250000000;
			
			System.out.println("The int outcome is " +e);
			
			
//Date type is float  storage is 4byte (accept decimal value) 
			
			float k =99.99f;
			
			System.out.println("The float outcome is " +k);
			
//Date type is double  storage is 8byte (accept decimal value) 
			
			double m=9999.888888;
			
			System.out.println("The double outcome is " +m);
			
	}
	
	

}
